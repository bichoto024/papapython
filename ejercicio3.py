# Si corres una carrera de 10 kilómetros en 42 minutos con 
# 42 segundos, ¿Cuál es tu ritmo promedio 
# (tiempo por cada milla en minutos y segundos)? 
# ¿Cuál es tu rapidez promedio en millas por hora?

